public class LoopWhiles {
  public static void main(String[] args) {
    // while (condition){
    // code block to be executed
    // }
    int first = 5;
    int second = 10;
    while (first <= second) {
      System.out.println(first);
      first = first + 1;
    }
    System.out.println("End");
    // return 0;
  }
}

